export const getFeaturedProducts = () => {
  return allProducts
    .filter((product) =>
      ["kucoin-crypto", "paypal-business", "binance-crypto", "coinbase-crypto", "stripe-business", "wise"].includes(
        product.id,
      ),
    )
    .map((product) => ({
      ...product,
      description: `Pre-verified ${product.name} account with KYC completed. No ban risk, fast delivery, best pricing.`,
      gradient: getGradientForRarity(product.rarity),
      rating: "4.9",
    }))
}

function getGradientForRarity(rarity: string) {
  switch (rarity) {
    case "Common":
      return "from-gray-500 to-gray-700"
    case "Uncommon":
      return "from-green-500 to-green-700"
    case "Rare":
      return "from-blue-500 to-blue-700"
    case "Epic":
      return "from-purple-500 to-purple-700"
    case "Legendary":
      return "from-orange-500 to-orange-700"
    case "Mythic":
      return "from-red-500 to-red-700"
    default:
      return "from-gray-500 to-gray-700"
  }
}
