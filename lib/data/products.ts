export const getSortedProducts = (products: typeof allProducts, sortBy = "price-asc") => {
  const sorted = [...products].sort((a, b) => {
    switch (sortBy) {
      case "price-asc":
        return a.price - b.price
      case "price-desc":
        return b.price - a.price
      case "name-asc":
        return a.name.localeCompare(b.name)
      case "name-desc":
        return b.name.localeCompare(a.name)
      case "verification-asc":
        const levelOrder = ["Basic", "Enhanced", "Premium", "Business", "Elite", "Platinum"]
        return levelOrder.indexOf(a.verificationLevel) - levelOrder.indexOf(b.verificationLevel)
      case "verification-desc":
        const levelOrderDesc = ["Platinum", "Elite", "Business", "Premium", "Enhanced", "Basic"]
        return levelOrderDesc.indexOf(a.verificationLevel) - levelOrderDesc.indexOf(b.verificationLevel)
      default:
        return 0
    }
  })
  return sorted
}

