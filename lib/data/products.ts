function getVerificationLevel(price: number): keyof typeof verificationLevels {
  if (price <= 100) return "Basic"
  if (price <= 200) return "Enhanced"
  if (price <= 400) return "Premium"
  if (price <= 700) return "Business"
  if (price <= 1200) return "Elite"
  return "Platinum"
}

